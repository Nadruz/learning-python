# Inteiro para flot
preco = 30
print(preco)

preco = float(preco)
print(preco)

preco = 10 / 2
print(preco)

# Float para inteiro
preco = 10.3
print(preco)

preco = (preco / 2)
print(preco)

preco = (preco // 2)
print(preco)

